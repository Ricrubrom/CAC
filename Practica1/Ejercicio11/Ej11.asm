ORG 1000H
TABLA DB "AIEIOUI0"
CHAR DB "I"
CAMBIO EQU 88 ;88=X EN ASCII

ORG 3000H
CONCAR: NOP
FOR: MOV CH, [BX]
CMP CH, 48 ;48=0 EN ASCII
JZ FIN
INC AH
CMP [BX], CL
JNZ SIG
INC AL
MOV BYTE PTR [BX], CAMBIO
SIG: INC BX
JMP FOR
FIN: RET

ORG 2000H
MOV BX, OFFSET TABLA
MOV AX, 0
MOV CL, CHAR
CALL CONCAR
HLT
END