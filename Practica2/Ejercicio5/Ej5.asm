ORG 3000H
ES_NUM: NOP
CMP BYTE PTR [BX], 30H
JS NOES
CMP BYTE PTR [BX], 3AH
JNS NOES
MOV DX, 0FFH
JMP FINR
NOES: MOV DX, 00H
FINR:RET


ORG 2000H
MOV BX, OFFSET MSJ
MOV AL, OFFSET MSJE-OFFSET MSJ
INT 7
MOV BX, OFFSET NUM
INT 6
CALL ES_NUM
CMP DX, 0FFH
JNZ ERROR
MOV AL, 1
INT 7
JMP FIN
ERROR: MOV BX, OFFSET MSJE
MOV AL, OFFSET FINAL-OFFSET MSJE
INT 7
FIN: MOV CL, NUM
INT 0
END
